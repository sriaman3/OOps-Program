package com.classthree;

public class SwitchClass {

	public static void main(String[] args) {
		
		int n =10, m=6;
		
		switch (n) {
		case 10:
			System.out.println("10 is printed");
			break; //to terminate the Switch statement
		case 5:
			System.out.println("5 is printed");
			break;
		default:
			System.out.println("default");
			break;
		}
	}

}
